#ifndef CLASS_TABLE_H_
#define CLASS_TABLE_H_

#include <unordered_map>
#include <cool-tree.h>
#include <singleton.h>

using namespace std;

static char* no_type = "_no_type"; 

//////////////////////////////////////////////////////////////////////
//
// Symbols
//
// For convenience, a large number of symbols are predefined here.
// These symbols include the primitive type and method names, as well
// as fixed names used by the runtime system.
//
//////////////////////////////////////////////////////////////////////
static Symbol 
    arg,
    arg2,
    Bool,
    concat,
    cool_abort,
    copyy,
    Int,
    in_int,
    in_string,
    IO,
    length,
    Main,
    main_meth,
    No_class,
    No_type,
    Object,
    out_int,
    out_string,
    prim_slot,
    self,
    SELF_TYPE,
    Str,
    str_field,
    substr,
    type_name,
    val;
//
// Initializing the predefined symbols.
//
static void initialize_constants(void)
{
    arg         = idtable.add_string("arg");
    arg2        = idtable.add_string("arg2");
    Bool        = idtable.add_string("Bool");
    concat      = idtable.add_string("concat");
    cool_abort  = idtable.add_string("abort");
    copyy        = idtable.add_string("copy");
    Int         = idtable.add_string("Int");
    in_int      = idtable.add_string("in_int");
    in_string   = idtable.add_string("in_string");
    IO          = idtable.add_string("IO");
    length      = idtable.add_string("length");
    Main        = idtable.add_string("Main");
    main_meth   = idtable.add_string("main");
    //   _no_class is a symbol that can't be the name of any 
    //   user-defined class.
    No_class    = idtable.add_string("_no_class");
    No_type     = idtable.add_string("_no_type");
    Object      = idtable.add_string("Object");
    out_int     = idtable.add_string("out_int");
    out_string  = idtable.add_string("out_string");
    prim_slot   = idtable.add_string("_prim_slot");
    self        = idtable.add_string("self");
    SELF_TYPE   = idtable.add_string("SELF_TYPE");
    Str         = idtable.add_string("String");
    str_field   = idtable.add_string("_str_field");
    substr      = idtable.add_string("substr");
    type_name   = idtable.add_string("type_name");
    val         = idtable.add_string("_val");
}


class ClassTable
{
    friend Singleton<ClassTable>;
    private:
        std::unordered_map<Symbol, Class_> classes;
        bool is_init = false;
    protected:
        ClassTable() { }
        ~ClassTable() { }

    private:
        void check_for_invalid_inheritance(void);
        void check_for_invalid_definitions(void);
        Classes install_basic_classes(Classes classes);

    public:
        void init(Classes classes);
        auto begin(void) { return classes.begin(); }
        auto end(void) { return  classes.end(); }
        bool contains(Symbol class_);
        Class_ operator[](Symbol sym) { return classes[sym]; }
};



#endif /*CLASS_TABLE_H_*/